"""Layer 2 — 作者身份与写作约束预设。

定义「这篇文章由谁来写」，包括：
- 作者人设（年龄、身份、性格）
- 写作约束（节奏、人称、立场要求）
- 场景化禁词（在 CorePrompts 基础上追加）

内置三套预设，用户也可以完全自定义。

使用方式::

    # 选一个内置预设
    writer = WriterPreset.tech_blogger()

    # 完全自定义
    writer = WriterPreset(
        name="我的博主",
        persona="...",
        writing_rules=["..."],
    )
"""

from __future__ import annotations

from dataclasses import dataclass, field


@dataclass
class WriterPreset:
    """作者身份预设（Layer 2）。

    Attributes:
        name: 预设名称，便于识别（如 "科技博主"）
        persona: 作者人设描述，会作为 system prompt 的角色定义
        reader_profile: 目标读者画像描述
        writing_rules: 写作约束列表（如节奏要求、人称要求等）
        forbidden_words_extra: 额外禁词，会与 CorePrompts.forbidden_words 合并
        polish_persona: 润色阶段的 system prompt 角色定义
    """

    name: str = "默认作者"

    persona: str = (
        "你是一位经验丰富的自媒体作者，善于将复杂话题转化为读者愿意读完的文章。"
    )

    reader_profile: str = (
        "25-40 岁、一二线城市、碎片化阅读习惯，偏好有干货有观点的内容。"
    )

    writing_rules: list[str] = field(default_factory=lambda: [
        "先把判断讲清楚，再展开论据，不写空泛套话",
        "优先使用具体事实、数据、案例或类比支撑观点",
        "表达要稳定，不炫技，不堆砌辞藻",
    ])

    forbidden_words_extra: list[str] = field(default_factory=list)
    """追加禁词——与 CorePrompts.forbidden_words 合并，不会覆盖。"""

    polish_checklist: list[str] | None = None
    """自定义润色清单。非 None 时完全替换 CorePrompts.polish_checklist。
    用于为不同人设提供差异化的「去 AI 味」策略，避免所有风格被同一套规则磨平。
    为 None 时回退到 CorePrompts.polish_checklist（默认行为）。
    """

    default_enable_humanize: bool = True
    """该人设默认是否开启去 AI 味润色。
    运行时若 WritingOptions.enable_humanize 显式传入 True/False，则以用户输入优先。
    """

    polish_persona: str = (
        "你是一位资深编辑，帮这位作者把草稿改成他自己说话的风格。"
    )

