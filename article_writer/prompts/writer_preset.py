                "把松散段落收紧成更清晰的判断-依据-结论结构",
                "扫描夸张表达和社媒套话，改成专业、直接、可验证的表达",
                "检查结尾是否回到结论本身，避免鸡汤式收尾",
                "绝对禁词检查：删除夸张热词和无信息量口头禅",
